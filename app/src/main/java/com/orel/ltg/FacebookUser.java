public class FacebookUser {

    private String link;
    private String name;
    private String id;

    public void setLink(String link) {
        this.link = link;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLink() {
        return link;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }
}
